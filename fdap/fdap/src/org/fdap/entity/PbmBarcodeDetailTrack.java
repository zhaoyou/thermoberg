package org.fdap.entity;

public class PbmBarcodeDetailTrack {
  private Long bcdId;
  private Long bcId;
  private Long goodsFullId;
  private Integer totalNum;
  private Long kid;
  private Short uploadStatus;
public Long getBcdId() {
	return bcdId;
}
public void setBcdId(Long bcdId) {
	this.bcdId = bcdId;
}
public Long getBcId() {
	return bcId;
}
public void setBcId(Long bcId) {
	this.bcId = bcId;
}
public Long getGoodsFullId() {
	return goodsFullId;
}
public void setGoodsFullId(Long goodsFullId) {
	this.goodsFullId = goodsFullId;
}
public Integer getTotalNum() {
	return totalNum;
}
public void setTotalNum(Integer totalNum) {
	this.totalNum = totalNum;
}
public Long getKid() {
	return kid;
}
public void setKid(Long kid) {
	this.kid = kid;
}
public Short getUploadStatus() {
	return uploadStatus;
}
public void setUploadStatus(Short uploadStatus) {
	this.uploadStatus = uploadStatus;
}
  
  
}
